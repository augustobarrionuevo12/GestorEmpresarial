// Este archivo gestiona los datos de los proveedores.

let providers = []; // Aquí se almacenan todos los proveedores.

// Esta función agrega un nuevo proveedor al arreglo 'providers'.
function addProvider(provider) {
    providers.push(provider);
}

// Esta función devuelve todos los proveedores almacenados.
function getProviders() {
    return providers;
}
